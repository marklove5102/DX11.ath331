#pragma once
#include <iostream>
#include <string>
#include <cstdlib>
#include <format>


////////////////////////////////////////////////////////////////////////////////////////////////////
// @brief StringUtils Class
////////////////////////////////////////////////////////////////////////////////////////////////////
class StringUtils
{
public:
	/// string 문자열을 wstring으로 변환한다.
	static std::wstring ConvertToWString( const std::string& str );

	/// wstring 문자열을 string으로 변환한다.
	static std::string ConvertToString( const std::wstring& wstr );

	/// 문자열을 int64형으로 반환한다.
	static __int64 GetAtInt64( std::string str );

	/// 문자열을 bool형으로 반환한다.
	static bool GetBool( std::string str );
};